function randomNum(minNum, maxNum) {//选题的范围
  // console.log("1234")
  switch (arguments.length) {
    case 1://只传入了最小值
      return parseInt(Math.random() * minNum + 1, 10);
      break;
    case 2://两个值都传入
      return parseInt(Math.random() * (maxNum - minNum + 1) + minNum, 10);//解析为10进制数
      break;
    default:
      return 0;
      break;
  }
}
